    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String productID = jLabel3.getText();
        String productname1 = jTextField1.getText();
        String price = jTextField4.getText();
        String description = jTextField5.getText();
        String cgst = jTextField3.getText();
        String sgst = jTextField6.getText();
        String wcgst = jTextField14.getText();
        String wsgst = jTextField15.getText();
        String hsnCode = jTextField7.getText();
        String bprice = jTextField8.getText();
        float Quantity = Float.parseFloat(jTextField2.getText());
        float wholeprice = Float.parseFloat(jTextField11.getText());
        String productname = productname1.trim();
        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO product VALUES('" + productID + "', '" + productname + "', '" + price + "', '" + description + "', '" + Quantity + "', '" + hsnCode + "', '" + cgst + "', '" + sgst + "', '" + bprice + "', '" + wholeprice + "', '" + wcgst + "', '" + wsgst + "') ;");
            //st.executeUpdate("ALTER TABLE stockreporting ADD "+productname+" varchar(255);");
            JOptionPane.showMessageDialog(null, "Successfully updated the product list");
            setVisible(false);
            new newProductForm().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

