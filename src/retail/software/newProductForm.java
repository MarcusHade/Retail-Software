    private void jTextField13KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField13KeyReleased
        float percent = Float.parseFloat(jTextField13.getText());
        float sp = Float.parseFloat(jTextField11.getText());
        float fp = (percent * sp / 100);
        String sfp = String.valueOf(fp);
        jTextField15.setText(sfp);
    }//GEN-LAST:event_jTextField13KeyReleased

